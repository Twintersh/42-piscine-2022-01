int	ft_str_is_alpha(char *str);

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(64 < str[i] && str[i] < 91))
			if (!(96 < str[i] && str[i] < 123))
				return (0);
		i++;
	}
	return (1);
}
