int	ft_str_is_lowercase(char *str);

int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(96 < str[i] && str[i] < 123))
			return (0);
		i++;
	}
	return (1);
}
