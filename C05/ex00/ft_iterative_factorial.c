int	ft_iterative_factorial(int nb)
{
	int	x;
	int	i;

	i = 0;
	x = 1;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (nb - 1)
	{
		i++;
		x *= (i + 1);
		nb--;
	}
	return (x);
}
